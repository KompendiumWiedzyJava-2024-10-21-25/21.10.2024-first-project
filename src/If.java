public class If {
    public static void main(String[] args) {
        int a = 2;

        if(a == 1) {
            System.out.println("to jest 1 !!!");
        } else if(a == 2) {
            System.out.println("to jest 2 !!!");
        } else if(a == 3) {
            System.out.println("to jest 3 !!!");
        } else {
            System.out.println("nie wiemy co to !!!");
        }

        System.out.println("Koniec programu !!");
    }
}
