public class Switch {
    public static void main(String[] args) {
        int a = 2;

        switch(a) {
            case 1:
                System.out.println("to jest 1 !!!");
                break;
            case 2:
                System.out.println("to jest 2 !!!");
                break;
            case 3:
                System.out.println("to jest 3 !!!");
                break;
            default:
                System.out.println("nie wiadomo co to !!!");
                break;
        }

        System.out.println("koniec programu !!");
    }
}
